                {
                    Console.Write("        ");
                }
                for (j = i; j >= 1; j--)
                {
                    Console.Write("{0}*{1}={2}\t", i, j, i * j);
                }
                Console.WriteLine("\t");
            }
            for (i = 1; i <= 9; i++)
            {
                for (j = 1; j <= i; j++)
                {
                    Console.Write("{0}*{1}={2}\t", i, j, i * j);
                }
                for (int k = 2 * (9 - i); k > 0; k--)
                {
                    Console.Write("        ");
                }
                for (j = i; j >= 1; j--)
                {
                    Console.Write("{0}*{1}={2}\t", i, j, i * j);
                }
                Console.WriteLine("\t");
            }
            Console.WriteLine("========================================");
            Console.ReadKey();
        }
    }
